#include <stdlib.h>
#include<time.h>
#include<stdio.h>
/**
 * main - entry point
 *
 * Return: 1 (FAILURE)
 */
int print_sign(int n){
	char result;
	if(n > 0){
		result = '+';
		return 1;
		printf(result);
}
else if(n = 0){
result = 0;
return 0;
printf(result);
}
else{
result = '-1';
return -1;
printf(result);
}
return 0;
}
